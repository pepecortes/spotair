<template lang="pug">
	extends FormBase.pug
	
	block input
			
		b-form-group(
			label="Modele",
			label-for="modele",
			:invalid-feedback="formData.invalid.modele",
			:state="checkValidityState($v.formData.modele)"
		)
			v-select(
				id="modele",
				:options="modeleOptions",
				label="text",
				v-model="formData.modele",
				:state="checkValidityState($v.formData.modele)"
			)
				span(slot="no options") Aucun r√©sultat
				
		b-form-group(
			label="Version",
			label-for="version",
			:invalid-feedback="formData.invalid.version",
			:state="checkValidityState($v.formData.version)"
		)
			b-form-input(
				id="version",
				type="text",
				v-model.trim="formData.version",
				:state="checkValidityState($v.formData.version)"
			)
				
</template>

<script>
import FormBase from './FormBase.vue'
import { required } from "vuelidate/lib/validators"

export default {
	
	extends: FormBase,
	
	data () {
		return {
			model: "avion",
			validations: {
				version: {required},
				modele: {required},
			},
			modeleOptions: []
		}
	},
	
	mounted() {
		this.getModeleOptions()
	},
	
	methods: {
		
		selectNewForm() {
			this.axios.get(`modeles/${this.mutableInitialId}`)
				.then(output => this.newForm({modele: output.data}))
				.catch(() => this.newForm())
		},
		
		getModeleOptions() {
			var vm = this
			vm.axios.get('modeles/')
				.then(response => vm.modeleOptions = response.data)
				.catch(err => vm.showAxiosAlert(err))
		},
	}
	
}
</script>
